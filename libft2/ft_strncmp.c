#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = (size_t)-1;
	if (!*s1 || !*s2)
		return ((unsigned char)*s1 - (unsigned char)*s2);
	while (++i < n)
		if (s1[i] != s2[i] || !s1[i] || !s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
	return (0);
}
